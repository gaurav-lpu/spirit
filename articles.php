						<p>The SPIRIT project has been going strong since the beginning although it started in the middle of the covid pandemic. A sport project at a time when nobody could step outside for something else than shopping was quite the challenge for the member of the consortium but due to high ownership of the partners and the strong rains from our coordinator Dr Mani, the programme meet with most of its targets on time.</p>	
                    <br>
                    <p>Our WP1 was completed, unfortunately without a single face to face meeting due to travel bans, with a strong cooperation between the Asian and European partners. The online “study visit” were a success with a good number of members from the Asian partners taking part in the study visits.</p>
                    <br>
                    <p>The consortium would like to share the summary of the WP1:</P>
					<br>
<p>Curriculum comparisons are very important for curriculum development on sports and nutrition education. The development of this curriculum is based on a comparison of the curriculum between universities in Asia and Europe. It provides the structure and content of developed curriculum from 11 universities that are included in SPIRIT Project.
</p>
<p>The main foal of WP 1 is to do the preparation and analysis to build a modular courses according to Asian HEIs’s need support by EU experts, best practices and activities in the area of inclusion in sport sciences and physical education and nutrition in sport and health sciences. The outcomes is a guidelines will be produced with recommendation to authorities towards doing steps on promoting inclusion through sport and physical education.
</p>
<p>This is a report in WP1 that will be the basic step to move to WP2, including three point based on WP1.2, WP 1.2, and WP1.3. Work package 1.1 presented a report about comparison of curricula in inclusive education through sport and physical activities between Asia and EU HEIs, WP 1.2 presented a report about virtual study visit to Europe, WP 1.3 presented a report about survey and report with recommendation for implementation of SPIRIT curriculum package and sharing knowledge platform for curriculum development and elaboration of guidelines for inclusive universities. The last of WP1 which is WP1.4 is a report sharing knowledge platform for curriculum development and elaboration of guidelines for inclusive universities.
</p>
<p> Based on the data obtained, the courses and modules proposed by the Asian Institution partner universities will be four courses based on these needs, guidelines can be followed in the preparation of the curriculum.
</p>
<p> In the meantime, the partners were already organising events and international seminars online to promote the project. The dissemination of SPIRIT is actively done by all partners and it has been quite a success as per the metrics. Each Asian partner have also created their own SPIRIT student Sports Club which is being used as the main vector for activities for dissemination, reach and engagement with the project target groups.
</p>
<p> In the past one year and a half our partner have set up two conferences which have proven to be quite impactful tools to disseminate the project results with experts in the field. Online and offline activities done by partners (although offline were very limited due to the pandemic and the economic turmoil (for the Sri Lankan partners)) have also bore fruits with the support from the university authorities in setting up the club as well as the student enrolment in the club. Clubs in Indonesia and India have already started with basic activities such as small Yoga courses, working groups in hydration and nutrition with disable children. A course was also created as a small test in “Promoting physical activity and diet in combating triple burden of disease in Asia”. The first international conference happened on the 8-9th October 2021 with speakers from Spain, Indonesia, Malaysia, Belgium, India and Sri Lanka, this was organised by USU. The second conference was organised by BVP, Pune on “Sports and physical activity is a medicine for non-communicable disease with speakers from India. Another webinar from USU “the role of student in sports development to promote a healthy lifestyle and career planning”. The second international conference was organised ISBR, with speaker from India, USA, South Africa, Cyprus, Spain, Portugal, Indonesia and Sri Lanka. These major activities created a lot of exposure for the project and we saw an increase in our social media engagement and impression.
</p>
<p> The development WP (2 and 3) have also been going good with multiple courses crated by the partners. Here is a sample list of the courses developed: Exercise Science, Motor development, Physical Education in Sports in Inclusive Schools, entrepreneurship in sports, excellence is sports business management, marketing and communication in sports, exercise and nutrition management in cancer, wellness for senior care and healthy ageing, wellness integrated medicine, traditional sport and outdoor activities, innovative teaching strategies for inclusion, innovate and entrepreneurship in sports management, sport technology and industry, nutrition, exercise and healthy lifestyle. 
Those courses will be put on a online Moodle platform for members to follow them as well as anyone interested in them.
</p>


						<p>The project team members also had the opportunity to organise three face to face meeting once border were open. The first meeting took place at the Polytechnic university of Valencia, Spain from 9th to the 14th of May to, the second one the Universitas Sumatera Utara from the 16th of June to the 21st of June, and our last one at Lovely Professional University in Punjab, India from the 11th of July to the 17th of July. The meeting had for main goal to discuss about the organisation of the consortium and how to proceed with it. Each WP leader presented an update on what was happening in their WP and prepared a calendar of work. The main bulk of the meeting was training in the topic relating to sports at each university and how they work with other organisation as a method to bridge with the communities. Content on the online platform, on how to create courses following the Bologna principle, each partner deciding what content they have or looking at developing.
</p>
